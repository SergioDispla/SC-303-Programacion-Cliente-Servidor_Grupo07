    public void listarVehiculos(String cedulaCliente) {
        try {
            ConectarDB connect = new ConectarDB();//conexion a la base de datos
            Connection conexion = connect.conectarDB();//obtiene la conexion

            //Consulta SQL para seleccionar todos los registros de los productos
            String consulta = "SELECT placa,marca FROM vehiculos WHERE cedula=?";

            //Preparar la declaracion SQL
            PreparedStatement declaracion = conexion.prepareStatement(consulta);
            //establece parametro de cedula del cliente
            declaracion.setString(1, cedulaCliente);
            // Ejecutar la consulta y obtener el resultado
            ResultSet resultado = declaracion.executeQuery();

            //Imprimir los resultados en la tabla
            while (resultado.next()) {
                String placa = resultado.getString("placa");
                String marca = resultado.getString("marca");

                vehiculosDefaultModel.addRow(new Object[]{placa, marca});

            }
            // Cerrar la conexi√≥n
            conexion.close();
            resultado.close();
            declaracion.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
